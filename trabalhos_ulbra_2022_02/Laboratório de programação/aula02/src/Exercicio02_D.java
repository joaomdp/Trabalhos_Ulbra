public class Exercicio02_D {
    public static void main(String[] args) {
        int tabuada = 9;
        for (int i=0;i<=10;i++){
            System.out.println("Tabuada do: "+tabuada);
            System.out.println(i + "X" + tabuada + "=");
            System.out.println(i * tabuada);
        }
    }
}
